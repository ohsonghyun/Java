package e_oop;

public class Calculator {
	double add(double a, double b){
		return a + b;
	}
	
	double sub(double a, double b){
		return a - b;
	}
	
	double mul(double a, double b){
		return (a * b);
	}
	
	double divi(double a, double b){
		return (a / b);
	}
	
	double rest(double a, double b){
		return a % b;
	}
}
